
document.getElementById(`purple0`).addEventListener('click', function () {
    if ($(`#purple0`).hasClass('purpleBoxClicked')) {
        $(`#purple0`).removeClass('purpleBoxClicked');
    } else {
        $(`#purple0`).addClass('purpleBoxClicked');
    }
})

